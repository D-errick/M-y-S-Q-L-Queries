# order activities
SELECT o.customer_id,
       o.payment_method,
       p.price,
       od.quantity,
       p.name,
       pt.category,
       o.order_date,
       a.delivery_address1,
       a.delivery_address2,
       a.delivery_zipcode,
       a.delivery_city
FROM orders o 
LEFT JOIN order_details od
ON o.order_id = od.order_id
LEFT JOIN pizzas p
ON od.pizza_id = p.pizza_id
LEFT JOIN pizza_types pt
ON p.name = pt.name
LEFT JOIN address a
ON o.add_id = a.add_id
WHERE p.price IS NOT NULL;

# employee details

WITH employee_shifts AS (
SELECT st.shift_date,
       DAYNAME(st.shift_date) AS Day_Name,
       CONCAT(s.first_name, " ", s.last_name) AS Full_name,
       s.employ_role,
       s.hourly_rate,
	   TIMESTAMPDIFF(HOUR, st.start_time, st.end_time) as hrs_worked
FROM staff s 
LEFT JOIN rota r 
ON s.staff_id = r.staff_id
LEFT JOIN shift st
ON r.shift_id = st.shift_id
GROUP BY 1,2,3,4,5,6
)
SELECT *,
       hourly_rate * hrs_worked AS total_salary
FROM employee_shifts;
